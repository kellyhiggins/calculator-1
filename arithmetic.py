def add(num1, num2):
    """adds num1 and num2, returning the resulting sum"""
    return num1 + num2

def subtract(num1, num2):
    """subtracts num2 from num1, returning the result"""
    return num1 - num2

def multiply(num1, num2):
    """multiplies num1 and num2, returning the result"""
    return num1 * num2

def divide(num1, num2):
    """divides num2 from num1, returning the result"""
    return float(num1) / num2

def square(num1):
    """multiplies num1 by itself, returning the result"""
    return num1 * num1

def cube(num1):
    """takes num1 and multiplies it by itself three times, returning the result"""
    return num1 ** 3

def power(num1, num2):
    """takes num1 to the power of num2, returning the result"""
    return num1 ** num2

def mod(num1, num2):
    """divides num2 from num1, returning the remainder of the division"""
    return num1 % num2